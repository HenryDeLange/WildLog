    public ReportingDialog(Frame inParent,
            Location inLocationToMap,
            Element inElementToMap) {
        super(inParent);
        // Set passed in values
        app = (WildLogApp) Application.getInstance();
        location = inLocationToMap;
        element = inElementToMap;

        // Auto generated code
        initComponents();

        // Determine what buttons to show
        if (location == null) {
            btnLocationMoon.setVisible(false);
            btnLocationSun.setVisible(false);
            btnLocationType.setVisible(false);
            btnLocationSpeciesCurve.setVisible(false);
        }

        // Setup the escape key
        final JDialog thisHandler = (JDialog)this;
        ActionListener escListiner = new ActionListener() {
                    @Override
                    public void actionPerformed(ActionEvent e) {
                        thisHandler.dispose();
                    }
                };
        thisHandler.getRootPane().registerKeyboardAction(
                escListiner,
                KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0),
                JComponent.WHEN_IN_FOCUSED_WINDOW);

        // Position the dialog
        Utils.setDialogToCenter(app.getMainFrame(), thisHandler);

        // Pack
        pack();
    }

