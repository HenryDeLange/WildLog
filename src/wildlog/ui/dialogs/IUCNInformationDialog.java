    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // Load IUCN data
        SwingUtilities.invokeLater(new Runnable() {
            @Override
            public void run() {
                try {
                    InputStream inputStream = new URL("http://apiv3.iucnredlist.org/api/v3/species/"
                            + URLEncoder.encode(scientificName, "UTF-8").replace("+", "%20")
// TODO: Use real token once IUCN replies to my email request...
                            + "?token=9bb4facb6d23f48efbf424bb05c0c1ef1cf6f468393bc745d42179ac4aca5fee").openStream();
                    data = new Gson().fromJson(new InputStreamReader(inputStream, "UTF-8"), IUCNSpeciesData.class);
                }
                catch (IOException | JsonIOException ex) {
                    WildLogApp.LOGGER.log(Level.ERROR, ex.toString(), ex);
                }
                if (data != null && data.getResult() != null && !data.getResult().isEmpty()) {
                    lblOtherName.setText(data.getResult().get(0).getMain_common_name());
                    lblEndangeredStatus.setText(data.getResult().get(0).getCategory());
                    lblYear.setText(Integer.toString(data.getResult().get(0).getPublished_year()));
                    lblResults.setText(Integer.toString(data.getResult().size()));
                    if (WildLogApp.getApplication().getWildLogOptions().isEnableSounds()) {
                        Toolkit.getDefaultToolkit().beep();
                    }
                    btnUpdateName.setEnabled(true);
                    btnUpdateEndangeredStatus.setEnabled(true);
                }
                else {
                    lblOtherName.setText("Not found.");
                    lblEndangeredStatus.setText("Not found.");
                    lblYear.setText("Not found.");
                    lblResults.setText("Not found.");
                }
            }
        });
    }//GEN-LAST:event_formWindowOpened

