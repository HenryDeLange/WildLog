    private void btnViewWebpageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewWebpageActionPerformed
        try {
            Desktop.getDesktop().browse(URI.create("http://apiv3.iucnredlist.org/api/v3/website/"
                    + URLEncoder.encode(scientificName, "UTF-8").replace("+", "%20")));
        }
        catch (IOException ex) {
            WildLogApp.LOGGER.log(Level.ERROR, ex.toString(), ex);
        }
    }//GEN-LAST:event_btnViewWebpageActionPerformed

    private void btnUpdateNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateNameActionPerformed
        if (data != null && data.getResult() != null && !data.getResult().isEmpty()) {
            txtOtherName.setText(data.getResult().get(0).getMain_common_name());
            if (WildLogApp.getApplication().getWildLogOptions().isEnableSounds()) {
                Toolkit.getDefaultToolkit().beep();
            }
        }
    }//GEN-LAST:event_btnUpdateNameActionPerformed

    private void btnUpdateEndangeredStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateEndangeredStatusActionPerformed
        if (data != null && data.getResult() != null && !data.getResult().isEmpty()) {
            EndangeredStatus status = EndangeredStatus.getEnumFromText(data.getResult().get(0).getCategory());
            cmbEndangeredStatus.setSelectedItem(status);
            if (WildLogApp.getApplication().getWildLogOptions().isEnableSounds()) {
                Toolkit.getDefaultToolkit().beep();
            }
        }
    }//GEN-LAST:event_btnUpdateEndangeredStatusActionPerformed

