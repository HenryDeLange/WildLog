    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        if (originalTokenText != null && txtToken.getText() != null 
                && !txtToken.getText().trim().isEmpty() 
                && !originalTokenText.equals(txtToken.getText())) {
            WildLogApp.setINaturalistToken(txtToken.getText().trim());
        }
        setVisible(false);
        dispose();
    }//GEN-LAST:event_btnOKActionPerformed

    private void btnOpenAuthLinkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOpenAuthLinkActionPerformed
        try {
            Desktop.getDesktop().browse(URI.create(INAT_AUTH_URL));
        }
        catch (IOException ex) {
            WildLogApp.LOGGER.log(Level.ERROR, ex.toString(), ex);
        }
    }//GEN-LAST:event_btnOpenAuthLinkActionPerformed

